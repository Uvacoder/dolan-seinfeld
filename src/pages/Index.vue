<p class="font-normal">POLICE OFFICER: Halt!  Stop what you’re doing! We got word that an illegal human trafficking operation was underway in Central Park. You’re all under arrest for conspiracy to illegally traffic human cargo across the border to Canada. Let’s go.</p>
<p class="font-normal">UNCLE LEO: [with a look of horror] No, I can’t go back there!</p>
<p class="font-normal">[Uncle Leo turns to run. He immediately slips on a pile of wet leaves and goes down with a thud and a painful groan.]</p>
<p class="font-normal">ELAINE [rolling her eyes and turning around]: Oh brother!</p>
<p class="font-normal">[Setting: Police station. A number of officers are mulling around, wearing masks. George, Kramer, Elaine, Chad, Newman, and Uncle Leo are sitting in a cell together, looking cramped and uncomfortable.]</p>
<p class="font-normal">POLICE OFFICER: Well, we’re out of cells so you six will have to be quarantined together until we can release you. Could be a couple of days.</p>
<p class="font-normal">[He walks away.]</p>
<p class="font-normal">ELAINE: [looking around suspiciously] What I want to know is how they knew we were going to be in Central Park. The only people who knew we were going there were me, Chad, and Uncle Leo. Except…Kramer! You told the police!?</p>
<p class="font-normal">KRAMER: [looking abashed] They threatened me Elaine! Said I had to tell them everything I knew!</p>
<p class="font-normal">ELAINE: [menacingly] I think they meant about your illegal steak smuggling operation, not every illegal activity you’ve ever been aware of.</p>
<p class="font-normal">KRAMER: [looking thoughtful] Now that I think about it, yes, they may have meant that. I may have sold you out Elaine, but not before I was sold out myself!  Who else knew I was running Steaks with Newman?  Who talked?</p>
<p class="font-normal">[Kramer looks around. George looks guilty sitting in the corner. They all turn and look at him.]</p>
<p class="font-normal">GEORGE:[squirming under their gazes] It wasn’t my fault! They threw me in here and I couldn’t think of anything else to say.</p>
<p class="font-normal">NEWMAN: [his voice rising] So you gave us up?  You son of a -</p>


<p class="font-normal">KRAMER: Newman, no!</p>
<p class="font-normal">[Newman, stumpy arms swinging wildly, starts towards George, who shirks back. The others restrain Newman.]</p>
<p class="font-normal">GEORGE: [pleadingly] I can’t help it… I come from a long line of squealers!  My father was a squealer! And my grandfather before him! It’s long been a family secret that Deep Throat was a Constanza.</p>
<p class="font-normal">[The police officer walks back up.]</p>
<p class="font-normal">POLICE OFFICER: [looking at George] OK you were here first, so you’re the first to get a phone call.</p>
<p class="font-normal">GEORGE: [looking at the others] Don’t worry, Jerry will get us out of this.</p>
<p class="font-normal">[George walks out of the cell and to the phone. He picks it up and calls Jerry.]</p>
<p class="font-normal">JERRY: Hello?</p>
<p class="font-normal">GEORGE: [in an urgent tone] Jerry! Jerry, you gotta help us! They got us all locked up down here!  They have Uncle Leo!</p>
<p class="font-normal">JERRY: Who is this?</p>
<p class="font-normal">GEORGE: Jerry! You know very well who this is! We’re all stuck down here at the police station and you need to come get us.</p>
<p class="font-normal">JERRY: Let me get this straight. You all decided to go on a little crime spree while the whole city was on lockdown and now you’re all in jail.  And you want me to come down there to a virus- infested police station and bail you all out! Well I won’t do it.</p>
<p class="font-normal">GEORGE: [pleading] Jerry you have to come! We’ll be stuck in here all night if you don’t.</p>
<p class="font-normal">JERRY: Well that’s a shame. Goodnight George!</p>
<p class="font-normal">[Jerry hangs up. George looks disappointedly back at the group. He walks back to the cell and sits down as the officer closes the gate and the others glare at him.]</p>
<p class="font-normal">CHAD: [squirming with his legs crossed] Elaine, may I please use the bathroom?</p>
<p class="font-normal">[Elaine looks at Chad with disgust, and slumps back into her chair].</p>
<p class="font-normal">GEORGE: [looking around] Anybody have some mints?</p>


