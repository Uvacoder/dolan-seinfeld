<p><span style="font-weight: 400;">NEWMAN: [with an air of superiority] Double corrugated? Kramer, I can assure you that the United States Postal Service uses only the finest boxes available.&nbsp;</span></p>
 
<p><span style="font-weight: 400;">KRAMER: So why are these boxes breaking apart?</span></p>
 
<p><span style="font-weight: 400;">NEWMAN: [shrugging] Budget cuts.&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Just then alarms sound and flashing lights are seen.&nbsp; A number of police cars pull up. A spotlight shines on Kramer and Newman, who are frozen in panic.]</span></p>
 
<p><span style="font-weight: 400;">POLICE MEGAPHONE: Police! Stay where you are!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">Newman turns to run but makes it about 15 feet and then stops, panting with his hands on his knees.&nbsp;</span></p>
 
<p><span style="font-weight: 400;">NEWMAN: Curse you Ben and Jerry&rsquo;s! Why must you be so delicious!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Kramer also turns to run, but slips on steaks and ice that are all over the ground and falls down. A police officer in a mask stands them both up and puts them in handcuffs.]</span></p>
 
<p><span style="font-weight: 400;">POLICE OFFICER: We got word that some steaks were going to be moved by illegally using a U.S. Postal Service vehicle for a personal trip. And during a pandemic! This offense implicates the State of New York, the United States Postal Service, and the U.S. Department of Agriculture...and we got you boys in a triple play in one fell swoop! Well, what do you have to say for yourselves?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">KRAMER: [with a weak smile and holding up a trampled, dirty steak] Could I interest you in some Grade A Omaha steak, officer?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Scene ends.]</span></p>
 
<p><span style="font-weight: 400;">[Elaine is sitting on the couch in her apartment. It&rsquo;s dark and quiet.]</span></p>
 
<p><span style="font-weight: 400;">ELAINE: [Peering into the bedroom] Finally, I thought he&rsquo;d never fall asleep. I have to get this guy out of here.</span></p>
 
<p><span style="font-weight: 400;">[She picks up the phone and dials a number].&nbsp;</span></p>
 
<p><span style="font-weight: 400;">ELAINE: Hello? Is this the Goose?</span></p>
 
<p><span style="font-weight: 400;">MYSTERIOUS DEEP VOICE: They call me the Goose, yes. Are you ready to proceed with the extraction?&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">ELAINE: Yes, yes, fine.&nbsp;</span></p>
 
<p><span style="font-weight: 400;">MYSTERIOUS DEEP VOICE: Do you have what I asked for?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">ELAINE: Yes, I have it.&nbsp;</span></p>
 
<p><span style="font-weight: 400;">MYSTERIOUS DEEP VOICE: Meet me in Central Park at midnight. Come alone.&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">ELAINE: Um shouldn&rsquo;t I bring him with me?</span></p>
 
<p><span style="font-weight: 400;">MYSTERIOUS DEEP VOICE:&nbsp; Oh&hellip;right, yes. Bring him with you. Do not be late!&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[The phone call ends. Elaine walks into the bedroom.]</span></p>
 
<p><span style="font-weight: 400;">ELAINE: Chad? Chad?&nbsp; Wake up! [shakes him] Pack up your things! We&rsquo;re going for a little ride.&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Elaine smiles deviously as Chad awakens with a confused look.]&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Scene ends.]</span></p>
 
<p><span style="font-weight: 400;">[Setting: Central Park, it&rsquo;s dark outside, nearly midnight. Elaine and a confused looking Chad walk into a secluded area. Another figure, hooded and mysterious, approaches them.]</span></p>
 
<p><span style="font-weight: 400;">MYSTERIOUS FIGURE: Did you bring what I asked?</span></p>
 
<p><span style="font-weight: 400;">ELAINE [taking a package out]: Yeah, I have what you wanted. You&rsquo;re the Goose?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">MYSTERIOUS FIGURE: [eyeing the package greedily] Yes, they call me the Goose. That stuff is like gold right now. Well, hand it over!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[The man reaches forward to reveal a large package of toilet paper. As the man reaches forward, a gust of wind knocks the man&rsquo;s hood off. Elaine squints in the darkness as she recognizes the figure.]</span></p>
 
<p><span style="font-weight: 400;">ELAINE: Uncle Leo? Is that you? You&rsquo;re the Goose?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">UNCLE LEO: [looking abashed] Elaine! I, uh, yes, well I used to run people across the border back in the 70s. I thought maybe I could earn a little extra picking it back up again.&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">CHAD: Elaine, you were going to have this man smuggle me back into Canada? You know they&rsquo;re letting Canadian citizens back across the border right?&nbsp; I thought you wanted to spend time with me. You could have just asked me to leave if you didn&rsquo;t want me around, eh?&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">ELAINE [turning to Chad and erupting]: You knew you could go home this whole time and you&rsquo;ve been staying at my apartment?!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[She swings her empty bag at him. As she does, a masked police officer runs towards them]</span></p>
 
<p><span style="font-weight: 400;">POLICE OFFICER: Halt!&nbsp; Stop what you&rsquo;re doing! We got word that an illegal human trafficking operation was underway in Central Park. You&rsquo;re all under arrest for conspiracy to illegally traffic human cargo across the border to Canada. Let&rsquo;s go.</span></p>
 
<p><span style="font-weight: 400;">UNCLE LEO: [with a look of horror] No, I can&rsquo;t go back there!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Uncle Leo turns to run. He immediately slips on a pile of wet leaves and goes down with a thud and a painful groan.]</span></p>
 
<p><span style="font-weight: 400;">ELAINE [rolling her eyes and turning around]: Oh brother!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Setting: Police station. A number of officers are mulling around, wearing masks. George, Kramer, Elaine, Chad, Newman, and Uncle Leo are sitting in a cell together, looking cramped and uncomfortable.]</span></p>
 
<p><span style="font-weight: 400;">POLICE OFFICER: Well, we&rsquo;re out of cells so you six will have to be quarantined together until we can release you. Could be a couple of days.&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[He walks away.]</span></p>
 
<p><span style="font-weight: 400;">ELAINE: What I want to know is how they knew we were going to be in Central Park. The only people who knew we were going there were me, Chad, and Uncle Leo. Except&hellip;Kramer! You told the police!?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">KRAMER: [looking abashed] They threatened me Elaine! Said I had to tell them everything I knew!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">ELAINE: [menacingly] I think they meant about your illegal steak smuggling operation, not every illegal activity you&rsquo;ve ever been aware of.</span></p>
 
<p><span style="font-weight: 400;">KRAMER: [looking thoughtful] Now that I think about it, yes, they may have meant that. I may have sold you out Elaine, but not before I was sold out myself!&nbsp; Who else knew I was running Steaks with Newman?&nbsp; Who talked?</span></p>
 
<p><span style="font-weight: 400;">[Kramer looks around. George looks guilty sitting in the corner. They all turn and look at him.]</span></p>
 
<p><span style="font-weight: 400;">GEORGE:[squirming under their gazes] It wasn&rsquo;t my fault! They threw me in here and I couldn&rsquo;t think of anything else to say.&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">NEWMAN: [his voice rising] So you gave us up?&nbsp; You son of a -&nbsp;</span></p>
 
<p><span style="font-weight: 400;">KRAMER: Newman, no!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Newman, stumpy arms swinging wildly, starts towards George, who shirks back. The others restrain Newman.]</span></p>
 
<p><span style="font-weight: 400;">GEORGE: I can&rsquo;t help it&hellip; I come from a long line of squealers!&nbsp; My father was a squealer! And my grandfather before him! It&rsquo;s long been a family secret that Deep Throat was a Constanza.&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[The police officer walks back up.]</span></p>
 
<p><span style="font-weight: 400;">POLICE OFFICER: [looking at George] OK you were here first, so you&rsquo;re the first to get a phone call.&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">GEORGE: [looking at the others] Don&rsquo;t worry, Jerry will get us out of this.&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[George walks out of the cell and to the phone. He picks it up and calls Jerry.]</span></p>
 
<p><span style="font-weight: 400;">JERRY: Hello?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">GEORGE: Jerry!&nbsp; Jerry, you gotta help us! They got us all locked up down here!&nbsp; They have Uncle Leo!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">JERRY: Who is this?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">GEORGE: Jerry! You know very well who this is! We&rsquo;re all stuck down here at the police station and you need to come get us.&nbsp;</span></p>
 
<p><span style="font-weight: 400;">JERRY: Let me get this straight. You all decided to go on a little crime spree while the whole city was locked down and now you&rsquo;re in jail.&nbsp; And you want me to come down there to a virus- infested police station and bail you all out! Well I won&rsquo;t do it.&nbsp;&nbsp;</span></p>
 
<p><span style="font-weight: 400;">GEORGE: [pleading] Jerry you have to come! We&rsquo;ll be stuck in here all night if you don&rsquo;t.</span></p>
 
<p><span style="font-weight: 400;">JERRY: Well that&rsquo;s a shame. Goodnight George!&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Jerry hangs up. George looks disappointedly back at the group. He walks back to the cell and sits down as the officer closes the gate.]</span></p>
 
<p><span style="font-weight: 400;">CHAD: [squirming with his legs crossed] Elaine, may I please use the bathroom?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Elaine looks at Chad with disgust, and slumps back into her chair].&nbsp;</span></p>
 
<p><span style="font-weight: 400;">GEORGE: [looking around] Anybody have some mints?&nbsp;</span></p>
 
<p><span style="font-weight: 400;">[Episode ends]&nbsp;</span></p>

